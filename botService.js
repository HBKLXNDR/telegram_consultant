                `–ù–∞—à–µ –ø–æ—Ä—Ç—Ñ–æ–ª—ñ–æ –¥–æ—Å—Ç—É–ø–Ω–µ –Ω–∞ —Å–∞–π—Ç—ñ: ${CONFIG.HOMEPAGE_URL}/portfolio\n\n–¢–∞–∫–æ–∂ –≤–∏ –º–æ–∂–µ—Ç–µ –ø–µ—Ä–µ–≥–ª—è–Ω—É—Ç–∏ –Ω–∞—à—ñ –ø—Ä–æ–µ–∫—Ç–∏ –≤ —Ç–µ–ª–µ–≥—Ä–∞–º –∫–∞–Ω–∞–ª—ñ: @our_portfolio`
